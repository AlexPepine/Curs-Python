# Tema Data Structures

my_list = [7, 8, 9, 2, 3, 1, 4, 10, 5, 6]
print(my_list)
my_list.sort()
print(my_list)
my_list.sort(reverse= True)
print(my_list)
my_list_slice = my_list[::-1]
print(my_list_slice)
my_list_slice = my_list[::1]
print(my_list_slice)
my_list_slice = my_list[::3]
print(my_list_slice)